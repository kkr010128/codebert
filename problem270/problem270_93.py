def main():
    S = input()

    if S == "SUN":
        print(7)
        exit(0)
    elif S == "MON":
        print(6)
        exit(0)
    elif S == "TUE":
        print(5)
        exit(0)
    elif S == "WED":
        print(4)
        exit(0)
    elif S == "THU":
        print(3)
        exit(0)
    elif S == "FRI":
        print(2)
        exit(0)
    else:
        print(1)
        exit(0)


if __name__ == "__main__":
    main()
