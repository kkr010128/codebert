def rpn(formula):
    stack = Stack()
    
    for d in formula:
        if d == "+":
            stack.push(stack.pop() + stack.pop())
        elif d == "-":
            stack.push(-stack.pop() + stack.pop())
        elif d == "*":
            stack.push(stack.pop() * stack.pop())
        else:
            stack.push(int(d))
            
    return stack.pop()

class Stack:
    def __init__(self):
        self.data = []

    def push(self, d):
        #print(self.data)
        self.data.append(d)
        
    def pop(self):
        #print(self.data)
        r = self.data[-1]
        self.data = self.data[:-1]
        return r
    
def main():
    formula = input().split()
    print(rpn(formula))
    
