# N=6とかで実験して、横で見ていたのを縦から眺めてみると良い（伝われ）
# (1+2+...+6) + (2+4+6) + ... + (6)と見る
# 結局何を足したいのか考えて約数のところを書き換える（伝われ）

N = int(input())
ans = 0

for i in range(1, N + 1):

    n = N // i
    temp = (n * (n + 1) * i) // 2
    ans += temp

print(ans)
