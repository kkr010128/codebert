N,M=map(int,input().split())
H=list(map(int,input().split()))
L=[0]*N
for i in range(M):
    A,B=map(int,input().split())
    L[A-1]=max(L[A-1],H[B-1])
    L[B-1]=max(L[B-1],H[A-1])
ans=0
for j in range(N):
    if H[j]>L[j]:
        ans+=1
print(ans)

        



    









        

    









    

        




    















    



    





    






        






    
















        


























            
    


    


    
    














    


    












