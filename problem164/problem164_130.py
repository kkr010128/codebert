# 高橋君と青木君がモンスターを闘わせます。
# 高橋君体力  A  攻撃力  B  です。
# 青木君体力  C  攻撃力  D  です。
# 高橋君→青木君→高橋君→青木君→... の順に攻撃を行います。
# このことをどちらかのモンスターの体力が  0  以下になるまで続けたとき、
# 先に自分のモンスターの体力が  0  以下になった方の負け、そうでない方の勝ちです。
# 高橋君が勝つなら Yes、負けるなら No を出力してくださ

a, b, c, d = list(map(int, input(). split()))#10 9 10 10

while True:
    c -= b
    if c  <= 0:
        print("Yes")
        break
    a -= d
    if a <= 0:
        print("No")
        break


