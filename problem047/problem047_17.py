import sys

output = []


while 1:
	a,op,b = input().split()
	a= int(a)
	b= int(b)

	if op=="+":
		output.append(a+b)
	elif op =="-":
		output.append(a-b)
	elif op =="*":
		output.append(a*b)
	elif op =="/":
		output.append( int(a/b) )
	elif op =="?":
		break;
	else:
		sys.exit()

for i in range( len(output)):
	print(output[i])