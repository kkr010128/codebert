n, m, l = map(int, input().split())
#   n x m ???????????????
A = [[0 for i in range(m)] for j in range(n)]
#   m x l ???????????????
B = [[0 for i in range(l)] for j in range(m)]
#   n x l ??????(????????????)?????????
C = [[0 for i in range(l)] for j in range(n)]

#   m x n ??????????¨??????\?????????????????????
for i in range(0, n):
    A[i][:m] = map(int, input().split())
    #   print(sheet_nxm)
#   m x n ??????????¨??????\?????????????????????
for i in range(0, m):
    B[i][:l] = map(int, input().split())
    #   print(sheet_mxl)
#   C(n x l)??????????¨???????A????????¨B????????????
for i in range(0, n):
    for j in range(0, l):
        for k in range(0, m):
            C[i][j] += A[i][k] * B[k][j]
#   print(C)
#   C???????????????
for i in range(0, n):
    for j in range(0, l):
        print("{0}".format(C[i][j]), end="")
        if j != l - 1:
            print(" ", end="")
        else:
            print("")