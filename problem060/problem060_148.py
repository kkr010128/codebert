inp = input().strip().split(" ")
n = int(inp[0])
m = int(inp[1])
l = int(inp[2])

# ?????????????????????????¢????
# A[n][m] B[m][l] C[n][l]
A = [[0 for i in range(m)] for j in range(n)]
B = [[0 for i in range(l)] for j in range(m)]
C = [[0 for i in range(l)] for j in range(n)]

# A???????????°??????????????????
for i in range(n):
    inp = input().strip().split(" ")
    for j in range(m):
        A[i][j] = int(inp[j])

# B???????????°??????????????????
for i in range(m):
    inp = input().strip().split(" ")
    for j in range(l):
        B[i][j] = int(inp[j])

# A x B
for i in range(n):
    for j in range(l):
        for k in range(m):
            C[i][j] += A[i][k] * B[k][j]
    print(" ".join(map(str,C[i])))