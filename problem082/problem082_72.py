S=input()
T=input()

ans=0

for i in range(len(S)-len(T)+1):
    c=0
    I=i
    t=0
    for j in range(len(T)):
        if S[I]==T[t]:
            c+=1
        t+=1
        I+=1
    ans=max(c,ans)

print(len(T)-ans)
        














    

                
            





    









        

    









    

        




    















    



    





    






        






    
















        


























            
    


    


    
    














    


    












