n = int(input())
alphabet = [chr(i) for i in range(97, 97+26)]
ans = ""
i = 0
# 次26**iを引いたらマイナスになるよ、というところで抜ける
while n - 26**i >= 0:
    n -= 26**i
    i += 1
# ここを抜けた時,iは桁数、nは「i桁の中でn番目」を表す

# したからk番目は、26**kで割った余り（に対応するアルファベット）
# 「上の桁で表現しきれなかった数」＝「その桁での表現」
#　これ、10進法をn進法に変換するアルゴリズムの正当性の説明を適用するとしっかりわかるわ！
for _ in range(i):
    q,r = divmod(n,26)
    n = q
    ans += alphabet[r]
print(ans[::-1])